# მათემატიკური ოპერაცია - Matematical operations

# + დამატება -- additon
# - გამოკლება -- subsraction
# * გამრავლება -- multiplication
# / გაყოფა -- Divasion

# + მიმატება -- Additon
print(25 + 10) #output: 35


# - გამოკლება -- Subraction
print(25 - 10) #Output: 25

# * გამრავლება -- multiplication
print(5 * 5) #output: 25

# / გაყოფა -- Division
print(10 / 2) #-- output: 5

# როდესაც ორ ინტეჯერს ვყოფთ, ამ გაყოფის შედეგი ყოველთვის იქნება Float.

